package school.repository.pagination;


import org.springframework.data.domain.Page;
import org.springframework.data.domain.Pageable;
import org.springframework.data.repository.PagingAndSortingRepository;
import org.springframework.stereotype.Repository;
import school.model.business.Faculty;


@Repository
public interface FacultySortedAndPagedRepository extends PagingAndSortingRepository<Faculty, Long> {
    Page<Faculty> findAll(Pageable pageable);

    Page<Faculty> findAllByNameContainsIgnoreCase(String nameContains, Pageable pageable);
}
